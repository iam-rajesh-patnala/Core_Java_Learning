package Chapter_1_Introduction_To_Java_Programming;

public class Introduction {
    public static void main(String[] args) throws Exception {

        System.out.println("");

        // Backslash escape sequences
        System.out.println("Backslash escape sequences");
        System.out.println("The backslash escape sequence \\n is used to print a new line.");
        System.out.println("The backslash escape sequence \\t is used to print a tab.");
        System.out.println("The backslash escape sequence \\\" is used to print a double quote.");
        System.out.println("The backslash escape sequence \\' is used to print a single quote.");
        System.out.println("The backslash escape sequence \\\\ is used to print a backslash.");
        System.out.println("The backslash escape sequence \\b is used to print a backspace.");
        System.out.println("The backslash escape sequence \\f is used to print a form feed.");
        System.out.println("The backslash escape sequence \\r is used to print a carriage return.");

    }
}
